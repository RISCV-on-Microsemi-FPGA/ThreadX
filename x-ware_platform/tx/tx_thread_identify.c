/*    This function returns the control block pointer of the currently    */ 
/*    executing thread.  If the return value is NULL, no thread is        */ 
/*    executing.                                                          */ 
/*                                                                        */ 
/*  INPUT                                                                 */ 
/*                                                                        */ 
/*    None                                                                */ 
/*                                                                        */ 
/*  OUTPUT                                                                */ 
/*                                                                        */ 
/*    TX_THREAD *                           Pointer to control block of   */ 
/*                                            currently executing thread  */ 
/*                                                                        */ 
